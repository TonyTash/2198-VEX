task autonomous()
{
	int defence = 0;
	pre_auton();
	while(SensorValue[clawSensor] < 1200)
	{
		motor[claw] = -90;
		armUp();
	}
	armStop();
	clawStop();
	while(SensorValue[armSensor] > 600)
	{
		forwardAuto(90);
		armUp();
	}
	armStop();
	while(SensorValue[leftWheels] < 1250 && SensorValue[rightWheels] < 1250)
	{
		forwardAuto(90);
	}
	stopMoving();
	armStop();
	wait1Msec(1000);
	clearEncoders();
	while(true)
	{
		defenseAuto();
	}
}


