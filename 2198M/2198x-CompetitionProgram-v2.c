task usercontrol()
{
	// User control code here, inside the loop

	while (true)
	{

    //Right side of the robot is controlled by the right joystick, Y-axis
    motor[driveLeftMotor] = vexRT[Ch2];
    //Left side of the robot is controlled by the left joystick, Y-axis
    motor[driveRightMotor] = vexRT[Ch3];


    // Control speeds from variable to make changes easier
    int mobileSpeed = 127;
    int towerSpeed = 127;
    int clawSpeed = 127;

	  //Tower Cone Control
    if(vexRT[Btn6U] == 1)
    {
      motor[towerRightPrim] = towerSpeed;
      motor[towerLeftPrim] = towerSpeed;
      motor[towerRightSecd] = towerSpeed;
      motor[towerLeftSecd] = towerSpeed;
    }
    else if(vexRT[Btn6D] == 1)
    {
      motor[towerRightPrim] = (-1)*(towerSpeed);
      motor[towerLeftPrim] = (-1)*(towerSpeed);
      motor[towerRightSecd] = (-1)*(towerSpeed);
      motor[towerLeftSecd] = (-1)*(towerSpeed);
    }
    else
    {
      motor[towerRightPrim] = 0;
      motor[towerLeftPrim] = 0;
      motor[towerRightSecd] = 0;
      motor[towerLeftSecd] = 0;
    }

    //Mobile Goal Control
    if(vexRT[Btn5U] == 1)
    {
      motor[mobileLeftMotor] = mobileSpeed;
      motor[mobileRightMotor] = mobileSpeed;
    }
    else if(vexRT[Btn5D] == 1)
    {
      motor[mobileLeftMotor] = (-1)*(mobileSpeed);
      motor[mobileRightMotor] = (-1)*(mobileSpeed);
    }
    else
    {
      motor[mobileLeftMotor] = 0;
      motor[mobileRightMotor] = 0;
    }

    //Claw Motor Control
    if(vexRT[Btn8L] == 1)
    {
      motor[clawLeft] = clawSpeed;
      motor[clawRightMotor] = clawSpeed;
    }
    else if(vexRT[Btn8R] == 1)
    {
      motor[clawLeftMotor] = (-1)*(clawSpeed);
      motor[clawRightMotor] = (-1)*(clawSpeed);
    }
    else
    {
      motor[clawLeftMotor] = 0;
      motor[clawRightMotor] = 0;
    }

	}
}
